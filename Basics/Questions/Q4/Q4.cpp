//Write a program to show typecasting in c++

#include<iostream>
using namespace std;
int main(){
  int a=45;
  float b=45.35;
  char c='A';
  cout<<"Float value of a :"<<(float)a;
  cout<<endl<<"Integer value of b : "<<(int)b;
  cout<<endl<<"Integer value of c : "<<(int)c;
  cout<<endl<<"Character value of a : "<<(char)a;
  return 0;
}